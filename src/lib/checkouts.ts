export const BOGEY_NUMBERS = new Set([169, 168, 166, 165, 163, 162, 159])

export function getCheckoutSuggestions(remaining: number, count = 1): string[] {
  if (BOGEY_NUMBERS.has(remaining) || !CHECKOUTS[remaining]) return []
  return CHECKOUTS[remaining].slice(0, count)
}

export function isFinishable(remaining: number): boolean {
  return remaining >= 2 && remaining <= 170 && !BOGEY_NUMBERS.has(remaining) && !!CHECKOUTS[remaining]
}

export function getScoreTag(remaining: number): 'finish' | 'bogey' | 'caution' | null {
  if (remaining <= 1) return null
  if (isFinishable(remaining)) return 'finish'
  if (BOGEY_NUMBERS.has(remaining)) return 'bogey'
  if (remaining > 170 && remaining <= 200) return 'caution'
  return null
}
